package com.matjo.web.rank.bean;

public class RankRestaBean {
	
	private String restaId;
	private String restaTitle;
	private String countRestaRank;
	private String restaImg;
	
	private String restaCate;
	
	public String getRestaId() {
		return restaId;
	}
	public void setRestaId(String restaId) {
		this.restaId = restaId;
	}
	public String getRestaTitle() {
		return restaTitle;
	}
	public void setRestaTitle(String restaTitle) {
		this.restaTitle = restaTitle;
	}
	public String getCountRestaRank() {
		return countRestaRank;
	}
	public void setCountRestaRank(String countRestaRank) {
		this.countRestaRank = countRestaRank;
	}
	public String getRestaImg() {
		return restaImg;
	}
	public void setRestaImg(String restaImg) {
		this.restaImg = restaImg;
	}
	public String getRestaCate() {
		return restaCate;
	}
	public void setRestaCate(String restaCate) {
		this.restaCate = restaCate;
	}
	
	
}
